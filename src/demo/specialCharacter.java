package demo;

public class specialCharacter {
    public static void main(String [] args) {
    	int _x = 10;
    	System.out.println(_x);
    	
     	int ___x = 10;
    	System.out.println(___x);
    	
     	int x$$$y = 10;
    	System.out.println(x$$$y);
    	
    	int x = 10__00;
    	System.out.println(x);
    	
    
    }
    
 	
}
